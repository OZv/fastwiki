#define is_page_start(p) \
		(0[p] == '<' && 1[p] == 'p' \
		 && 2[p] == 'a' && 3[p] == 'g' \
		 && 4[p] == 'e' && 5[p] == '>')

#define is_page_end(p) is_page_end_a(p)

#define is_page_end_a(p) \
		(0[p] == '<' && 1[p] == '/' \
		 && 2[p] == 'p' && 3[p] == 'a' \
		 && 4[p] == 'g' && 5[p] == 'e'\
		 && 6[p] == '>')

